n = int(input("Enter your number 1: "))
m = int(input("Enter your number 2: "))
ans1 = n + m
ans2 = n - m
ans3 = n * m
ans4 = n / m
ans5 = n % m
ans6 = n // m
ans7 = n ** m

print(n, "+", m, "=", ans1)
print(n, "-", m, "=", ans2)
print(n, "*", m, "=", ans3)
print(n, "/", m, "=", ans4)
print(n, "%", m, "=", ans5)
print(n, "//", m, "=", ans6)
print(n, "**", m, "=", ans7)
